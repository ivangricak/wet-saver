<h1>Privacy Policy</h1>
<p class="updated">Last updated: 14.06.2026</p>

<h2>1. Information we collect</h2>
<ul>
    <li>Username / nickname</li>
    <li>Login credentials (encrypted password storage)</li>
    <li>User-created content such as links, descriptions, names, and categories</li>
    <li>Groups of links created and managed by the user</li>
</ul>

<h2>2. How we use the information</h2>
<ul>
    <li>Authenticate users and manage accounts</li>
    <li>Store and organize user-generated content</li>
    <li>Provide core functionality of the extension</li>
    <li>Synchronize user data with the backend database</li>
</ul>

<h2>3. Data storage and security</h2>
<p>
    All data is stored in a SQL database hosted on Railway. Passwords are securely encrypted before storage.
    Data is transmitted over secure HTTPS connections. We apply reasonable security measures to protect user data.
</p>

<h2>4. Data sharing</h2>
<p>
    We do not sell, rent, or share personal data with third parties.
</p>

<p>
    The only exception is user-initiated sharing of groups of links between users inside the extension.
</p>

<h2>5. Permissions usage</h2>
<ul>
    <li><b>storage</b> – storing user preferences and data</li>
    <li><b>tabs</b> – interacting with browser tabs for extension functionality</li>
    <li><b>activeTab</b> – accessing current tab when user activates features</li>
    <li><b>contextMenus</b> – providing right-click actions</li>
    <li><b>sidePanel</b> – displaying extension interface</li>
</ul>

<h2>6. Data retention</h2>
<p>
    User data is stored as long as the account is active. Users may request deletion of their data at any time,
    after which all associated data will be permanently removed.
</p>

<h2>7. Third-party services</h2>
<p>
    This extension does not use third-party analytics or advertising services.
    The only external infrastructure used is Railway for database hosting.
</p>

<h2>8. User rights</h2>
<p>
    Users may request access, correction, or deletion of their data at any time by contacting support.
</p>

