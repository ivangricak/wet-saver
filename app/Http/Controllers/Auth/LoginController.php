    {
        $credentails = $request->validated();

        if(Auth::attempt($credentails)){
            $request->session()->regenerate();
            return redirect()->intended(route('home.index'));
        }

        return back()-> withError([
            'login'=>'bad login',
        ])->onlyInput('input');
